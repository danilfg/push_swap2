#include "libft.h"

t_bool	ft_isint(char *str)
{
	long	nbr;

	nbr = ft_atoi(str);
	if (ft_isnum(str, 10) && nbr >= -2147483648 && nbr <= 2147483647)
		return (true);
	return (false);
}
