#include "libft.h"

int		ft_sqrt_mod(int nb)
{
	int i;

	i = 0;
	if ((nb < 0) || (nb > 2147483647))
		return (0);
	while ((i * i) <= nb)
	{
		if ((i * i) == nb)
			return (i);
		i++;
	}
	return (i);
}
